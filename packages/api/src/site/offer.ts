export interface OfferListItem {
    title: string;
    alias: string;
    summary: string;
    photoUrl: string;
}

export interface OfferPhoto {
    url: string;
    altText: string | null;
}

export interface OfferEntry {
    title: string;
    description: string;
    photos: OfferPhoto[];
}

export const getOffersList = () => {
    const offer = offers[0];
    const photos = offerPhotos[offer.id]
        .map((p) => ({
            url: `/media/images/offer/${p.photourl}`,
            altText: p.alttext,
        }));

    const result: OfferEntry = {
        title: offer.title,
        description: offer.desc,
        photos,
    };
    return Promise.resolve(
        {
            offer: result,
            offers: offers.map(
                (o) =>
                    <OfferListItem>{
                        title: o.title,
                        alias: o.alias,
                        summary: o.descshort,
                        photoUrl: `/media/images/offer/${offerPhotos[o.id][0].photourl}`,
                    }
            )
        }
    );
}

export const getOffersAliases = () => Promise.resolve(offers.map((o) => o.alias));

export const getOffer = (alias: string) => {
    const offer = offers.filter((o) => o.alias === alias)[0];
    const photos = offerPhotos[offer.id]
        .map((p) => ({
            url: `/media/images/offer/${p.photourl}`,
            altText: p.alttext,
        }));

    const result: OfferEntry = {
        title: offer.title,
        description: offer.desc,
        photos,
    };
    return Promise.resolve(result);
};

export const offerExists = (alias?: string) => {
    return offers.filter((o) => o.alias === alias)[0] !== undefined;
};
